
public class Example7 {

	//Increment and Decrement Operators
	
	public static void main(String[] args) {
		
		int a = 10;
		int b = ++a; // pre increment
		
		System.out.println("a :"+a +"  "+"b :"+b);
		
		int c = 10;
		int d = c++; // post increment
		
		System.out.println("c :"+c+"  "+"d :"+d);
				
	}
}
