
public class Example8 {

	//Increment and Decrement Operators
	
	public static void main(String[] args) {
		
		int a = 10;
		int b = --a; // pre decrement
		
		System.out.println("a :"+a +"  "+"b :"+b);
		
		int c = 10;
		int d = c--; // post decrement
		
		System.out.println("c :"+c+"  "+"d :"+d);
				
	}
}
